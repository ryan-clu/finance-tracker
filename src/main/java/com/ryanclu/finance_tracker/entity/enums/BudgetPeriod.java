package com.ryanclu.finance_tracker.entity.enums;

public enum BudgetPeriod {
    WEEKLY,
    MONTHLY,
    YEARLY
}

/*
BudgetPeriod — defines the time window for a budget: WEEKLY, MONTHLY, YEARLY.
When a user sets a $500 grocery budget, this tells us whether that's $500 per week,
per month, or per year.
 */